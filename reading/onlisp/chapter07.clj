; Lisps are infamous for their seemingly goofy syntax, and one can certainly sympathize with the masses in the hate for parens.  In isolation, it makes very little sense for a language to adopt the syntax of Lisp as it can be quite awkward for simple things like say, mathematical equations (although, not having to memorize a gaggle of esoteric precedence rules is a big win).  However, when one considers Lisp's macro facility, the syntax suddenly makes a whole lot of good sense.
